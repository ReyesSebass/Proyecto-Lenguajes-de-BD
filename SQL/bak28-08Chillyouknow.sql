  SELECT 
    f.ID_FACTURA,
    f.FECHA_EMISION,
    p.ID_PEDIDO,
    u.NOMBRE AS NOMBRE_USUARIO,
    u.APELLIDO AS APELLIDO_USUARIO,
    f.TOTAL
FROM 
    FACTURAS f
JOIN 
    PEDIDOS p ON f.ID_PEDIDO = p.ID_PEDIDO
JOIN 
    USUARIOS u ON f.ID_USUARIO = u.ID_USUARIO
;
